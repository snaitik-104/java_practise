package assignments;
import java.util.Scanner;
public class practise_12 {


        public static int sum(int a, int b)
        {
            return a+b;
        }
        public static void main(String[] args)
        {
            int a1=2, b1=3;
            int res1=sum(a1,b1);
            System.out.print(res1+" ");

            int a2=4, b2=5;
            int res2=sum(a2,b2);
            System.out.print(res2);
        }

}

//sum() method will simply return the sum of both the numbers
// which are passed as an argument .Hence, the output of the program is 5 9.
