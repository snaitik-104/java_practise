package Tutorials;

public class float_double {
    public static void main(String[] args) {

        // float datatype
        float float_max = Float.MAX_VALUE;
        float float_min = Float.MIN_VALUE;

        System.out.println(float_max);
        System.out.println(float_min);

        //double datatype
        double double_max = Double.MAX_VALUE;
        double double_min = Double.MIN_VALUE;

        System.out.println(double_max);
        System.out.println(double_min);
    }
}
